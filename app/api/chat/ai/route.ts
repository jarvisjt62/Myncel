// Analyze conversation to understand context
function analyzeConversation(history: Message[], currentQuestion: string): string {
  if (!history || history.length === 0) return '';
  
  // Get the last few exchanges to understand context
  const recentHistory = history.slice(-6);
  const topics = new Set<string>();
  
  for (const msg of recentHistory) {
    const lower = msg.content.toLowerCase();
    if (lower.includes('machine') || lower.includes('equipment')) topics.add('equipment');
    if (lower.includes('work order') || lower.includes('maintenance')) topics.add('maintenance');
    if (lower.includes('pricing') || lower.includes('plan') || lower.includes('cost')) topics.add('pricing');
    if (lower.includes('team') || lower.includes('invite') || lower.includes('user')) topics.add('team');
    if (lower.includes('alert') || lower.includes('notification')) topics.add('alerts');
    if (lower.includes('report') || lower.includes('analytics')) topics.add('reports');
  }
  
  return Array.from(topics).join(', ');
}

