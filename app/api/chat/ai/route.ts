export async function POST(req: NextRequest) {
  try {
    const body = await req.json();
    const { question, history = [] } = body as { question: string; history?: Message[] };
    
    if (!question) {
      return NextResponse.json({ error: 'Question is required' }, { status: 400 });
    }
    
    // Analyze conversation context
    const conversationContext = analyzeConversation(history, question);
    const response = findBestResponse(question, conversationContext);
    
    return NextResponse.json({ response });
  } catch (error) {
    console.error('AI chat error:', error);
    return NextResponse.json({ 
      response: "I'm here to help! A support agent will respond to your question shortly." 
    }, { status: 200 });
  }
}